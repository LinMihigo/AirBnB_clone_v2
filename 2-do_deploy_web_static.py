#!/usr/bin/python3
"""
Fabfile that holds do_deploy

Attributes:
    env.hosts (list): holds web server ip addresses
"""
from fabric.api import env, put, run, sudo
import os.path

env.hosts = ['100.25.41.202', '100.25.205.60']


def do_deploy(archive_path):
    """
    Distributes an archive to web servers

    Args:
        archive_path (str): path to the archive to share

    Returns:
        True: if file exists at archive_path and no error occurs
        False: Otherwise!
    """
    if os.path.isfile(archive_path) is False:
        return False
    file = archive_path.split("/")[-1]
    name = file.split(".")[0]

    if put(archive_path, "/tmp/{}".format(file)).failed is True:
        return False
    if run("rm -rf /data/web_static/releases/{}/".
           format(name)).failed is True:
        return False
    if run("mkdir -p /data/web_static/releases/{}/".
           format(name)).failed is True:
        return False
    if run("tar -xzf /tmp/{} -C /data/web_static/releases/{}/".
           format(file, name)).failed is True:
        return False
    if run("rm /tmp/{}".format(file)).failed is True:
        return False
    if run("mv /data/web_static/releases/{}/web_static/* "
           "/data/web_static/releases/{}/".format(name, name)).failed is True:
        return False
    if run("rm -rf /data/web_static/releases/{}/web_static".
           format(name)).failed is True:
        return False
    if run("rm -rf /data/web_static/current").failed is True:
        return False
    if run("ln -s /data/web_static/releases/{}/ /data/web_static/current".
           format(name)).failed is True:
        return False
    return True
